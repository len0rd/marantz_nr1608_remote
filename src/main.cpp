#include <Arduino.h>
#include <IRremote.h>

IRsend irsend;

static unsigned int power_toggle[] = {0x0000, 0x0073, 0x0000, 0x0018, 0x0020, 0x0020, 0x0020, 0x0020, 0x0020, 0x0020, 0x0040, 0x0020, 0x0020, 0x0020, 0x0020, 0x0020, 0x0020, 0x0020, 0x0020, 0x0020, 0x0020, 0x0040, 0x0020, 0x0020, 0x0040, 0x0020, 0x0020, 0x0C5B, 0x0020, 0x0020, 0x0020, 0x0020, 0x0020, 0x0020, 0x0040, 0x0020, 0x0020, 0x0020, 0x0020, 0x0020, 0x0020, 0x0020, 0x0020, 0x0020, 0x0020, 0x0040, 0x0020, 0x0020, 0x0040, 0x0020, 0x0020, 0x0498};

void setup() {
  // put your setup code here, to run once:
}

void loop() {
  int khz = 38; // 38kHz carrier frequency for the NEC protocol
  
  irsend.sendRaw(power_toggle, sizeof(power_toggle) / sizeof(power_toggle[0]), khz); //Note the approach used to automatically calculate the size of the array.

  delay(10000); // 10s repeat
}
